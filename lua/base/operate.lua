-- 算术运算符
a = 21
b = 10
c = a + b
print(c)
c = a - b
print(c)
c = a * b
print(c)
c = a / b
print(c)
c = a % b
print(c)
c = a ^ b
print(c)
c = a ^ 2
print(c)
c = -a
print(c)

-- 关系运算符
if a == b then
    print("a == b")
else
    print("a ~= b")
end

if a ~= b then
    print("a ~= b")
else
    print("a == b")
end
